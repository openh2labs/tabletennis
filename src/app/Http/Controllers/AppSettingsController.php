     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        try{
            $setting = AppSettings::where('var_name', $id)->firstOrFail();
            return response()->json([
                'var_name' => $id,
                'var_value' => $setting->var_value
            ], 201);
        }catch(\Exception $e){
            return response()->json(['response' => "'$id' setting value not found in the db"], 404);
        }
    }

